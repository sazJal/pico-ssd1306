/* Global Variable */
static uint8_t ssd1306_addr = 0x3C;
uint8_t res;
static uint8_t ssdBuffer[(SSD1306_HEIGHT*SSD1306_WIDTH)>>3];
static uint8_t reg[3];
SSD_Coordinate_t coord;

int main(void){
    stdio_init_all(); // Initialise STD I/O for printing over serial

    gpio_init(25);
    gpio_set_dir(25, GPIO_OUT);
    
    // Configure the I2C Communication
    i2c_init(I2C_PORT, 100000);
    gpio_set_function(4, GPIO_FUNC_I2C);
    gpio_set_function(5, GPIO_FUNC_I2C);
    gpio_pull_up(4);
    gpio_pull_up(5);
    coord.currentX = 0;
    coord.currentY = 0;

    ssd1306_init(I2C_PORT,ssd1306_addr);
    ssd1306_fill(ssdBuffer, SSD_COLOR_BLACK, sizeof(ssdBuffer));
    ssd1306_drawBitmap(ssdBuffer, 0, 0, elon, 128, 64, SSD_COLOR_WHITE);
    ssd1306_putString("Be like Elon", ssdBuffer, &Font_7x10, &coord, SSD_COLOR_WHITE);
    ssd1306_update_screen(I2C_PORT, ssd1306_addr, ssdBuffer);
    gpio_put(25, 1); // Set pin 25 to high

    // Infinite Loop
    while(1){
        sleep_ms(1000);
    }
}

